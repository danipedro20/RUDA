    <h1>RUDA</h1>
    <p> <h2> Control académico y administrativo para instituciones Educativas.
        RUDA es un sistema desarrollado para sistematizar y automatizar los 
procesos académicos y administrativos en las Instituciones Educativas. 
Como herramienta, el objetivo de RUDA es gestionar los procesos 
internos y facilitar la coordinación y comunicación entre alumnos, docentes y cargos directivos para ofrecer información estadística 
sobre dichos procesos y facilitar la toma de decisiones en el proceso de gestión de la Institución.</h2></p>
</section>
